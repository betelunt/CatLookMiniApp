module.exports.getNutrientById = (id) => NUTRIENTS.find(n => n.id === id);
module.exports.getFoodsForNutrient = (nutrientId) => {
  const nutrient = NUTRIENTS.find(n => n.id === nutrientId);
  if (!nutrient) return [];
  const { getById } = require('./foods');
  return nutrient.foodIds.map(id => getById(id)).filter(Boolean);
};
